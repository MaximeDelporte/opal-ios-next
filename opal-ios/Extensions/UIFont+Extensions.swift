import Foundation
import UIKit

extension UIFont {
    
    static let bodyRegular = UIFont.systemFont(ofSize: 17, weight: .regular)
    static let bodyMedium = UIFont.systemFont(ofSize: 17, weight: .medium)
    static let bodySemibold = UIFont.systemFont(ofSize: 17, weight: .semibold)
    static let calloutRegular = UIFont.systemFont(ofSize: 16, weight: .regular)
    static let calloutSemibold = UIFont.systemFont(ofSize: 16, weight: .semibold)
    static let footnoteRegular = UIFont.systemFont(ofSize: 13, weight: .regular)
    static let footnoteSemibold = UIFont.systemFont(ofSize: 13, weight: .semibold)
    static let captionSemibold = UIFont.systemFont(ofSize: 11, weight: .semibold)
}
